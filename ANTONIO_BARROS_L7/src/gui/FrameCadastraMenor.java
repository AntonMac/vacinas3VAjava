    private void jButtonCadastrarMenorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarMenorActionPerformed
        // TODO add your handling code here:
        String vNome = jTextPacienteNome.getText();
        String vNascimento = jTextPacienteNascimento.getText();
        String vMenorCPF = jTextMenorCPF.getText();
        String vResponsavelCPF = jTextResponsavelCPF.getText();
        try{
            if(vNome.isBlank()||vNascimento.isBlank()||vMenorCPF.isBlank()||vResponsavelCPF.isBlank()){
                throw new CampoVazioException();
            }
            if(!verificaData(vNascimento)){
                throw new FormatoDataInvalidoException();
            }
            if(sistema.getAdulto(vResponsavelCPF)==null){
                throw new ResponsavelnvalidoException("Responsavel Invalido");
            }
            
            sistema.cadastrarPaciente(new Menor(vNome,convertData(vNascimento),vMenorCPF,sistema.getAdulto(vResponsavelCPF)));
            
            JOptionPane.showMessageDialog(rootPane, "Menor Cadastrado com Sucesso!");
            
            jTextPacienteNome.setText(null);
            jTextPacienteNascimento.setText(null);
            jTextMenorCPF.setText(null);
            jTextResponsavelCPF.setText(null);
            
        }catch(CampoVazioException | FormatoDataInvalidoException | ResponsavelnvalidoException | PacienteJaExisteException ex){
            
            JOptionPane.showMessageDialog(rootPane, "Paciente N√£o Cadastrado \n" + ex.getMessage());
            
        }
    }//GEN-LAST:event_jButtonCadastrarMenorActionPerformed

